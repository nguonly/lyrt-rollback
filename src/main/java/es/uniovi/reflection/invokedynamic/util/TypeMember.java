package es.uniovi.reflection.invokedynamic.util;

/**
 * Indicates the qualifier of a class member.
 * The qualifier could be methods, properties (set or get), and constructors.
 * 
   * @author Computational Reflection Research Group. University of Oviedo
  * @version 0.3
 *
 */
public enum TypeMember {
	
	
	Method,
	 Get_property,
	Set_property,
	Constructor;	

}
